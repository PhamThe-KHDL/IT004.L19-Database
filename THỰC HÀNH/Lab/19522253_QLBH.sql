	DECLARE @SoHD INT, @MaSP CHAR(4), @SoLuong INT, @TriGia MONEY
	--LAY THONG TIN CUA CTHD VUA MOI THEM VAO
	SELECT @SoHD = SOHD, @MaSP = MASP , @SoLuong = SL
	FROM INSERTED
	--TINH TRI GIA CUA SAN PHAM MOI THEM VAO HOADON
	SET @TriGia = @SoLuong * (SELECT GIA 
								FROM SANPHAM 
								WHERE MASP = @MaSP)
	--KHAI BAO MOT CURSOR DUYET QUA TAT CA CAC CTHD DA CO SAN TRONG HOADON
	DECLARE CUR_CTHD CURSOR 
	FOR
		SELECT MASP, SL
		FROM CTHD
		WHERE SOHD = @SoHD
	OPEN CUR_CTHD
	FETCH NEXT FROM CUR_CTHD
	INTO @MaSP, @SoLuong

	WHILE (@@FETCH_STATUS = 0)
	BEGIN
		--CONG DON TRI GIA CUA TUNG SAN PHAM VAO BIEN TriGia
		SET @TriGia = @TriGia + @SoLuong * (SELECT GIA 
											FROM SANPHAM 
											WHERE MASP = @MaSP)
		FETCH NEXT FROM CUR_CTHD
		INTO @MaSP, @SoLuong
	END
	CLOSE CUR_CTHD
	DEALLOCATE CUR_CTHD
	--TIEN HANH CAP NHAT LAI TRI GIA HOADON
	UPDATE HOADON 
	SET GIATRI = @TriGia
	WHERE SOHD = @SoHD
END 

