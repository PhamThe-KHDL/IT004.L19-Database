CREATE TRIGGER TRG_INS_HD ON HOADON
FOR INSERT
AS
BEGIN
	--CAU I.11-----------------------------------------------
	-- KHAI BAO CAC BIEN
	DECLARE @NGAYHD SMALLDATETIME, @MaKH CHAR(4), @NGAYDK SMALLDATETIME
	-- LAY THONG TIN CUA HOA DON VUA MOI THEM VAO
	SELECT @NGAYHD = NGHD, @MaKH = MAKH
	FROM INSERTED
	-- LAY THONG TIN KHACH HANG TUONG UNG 
	SELECT @NGAYDK = NGDK
	FROM KHACHHANG
	WHERE MAKH = @MaKH
	-- SO SANH
	IF (@NGAYHD < @NGAYDK) -- NEU NGAY HOA DON NHO HON DANG KY THANH VIEN
	BEGIN
		PRINT 'LOI: NGAY HOA DON KHONG HOP LE 11'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM MOI MOT HOA DON THANH CONG 11'
	END

	--CAU I.12-----------------------------------------------
	-- KHAI BAO CAC BIEN
	DECLARE @NGAYVL SMALLDATETIME, @MaNV CHAR(4)
	-- LAY THONG TIN CUA HOA DON VUA MOI THEM VAO
	SELECT @MaNV = MANV
	FROM INSERTED
	-- LAY THONG TIN NHAN VIEN TUONG UNG
	SELECT @NGAYVL = NGVL
	FROM NHANVIEN
	WHERE MANV = @MaNV
	-- SO SANH
	IF (@NGAYHD < @NGAYVL)
	BEGIN
		PRINT 'LOI: NGAY HOA DON KHONG HOP LE 12'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM MOI MOT HOA DON THANH CONG 12'
	END

	--CAU I.13-----------------------------------------------
	DECLARE @SoLuongCTHD INT = 0, @SoHD INT
	SELECT @SoHD = SOHD
	FROM INSERTED
	--DEM SO LUONG CTHD CUA MOI HOA DON
	SELECT @SoLuongCTHD = COUNT(SOHD)
	FROM CTHD
	WHERE @SoHD = SOHD
	GROUP BY SOHD
	-- SO SANH
	IF (@SoLuongCTHD < 1)
	BEGIN
		PRINT 'LOI: HOA DON KHONG HOP LE 13'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM MOI MOT HOA DON THANH CONG 13'
	END
END

